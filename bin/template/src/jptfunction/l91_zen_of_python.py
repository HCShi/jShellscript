##################################################################
## if 高级写法
a = 1 if 1 > 2 else 3; print(a)
##################################################################
## for ... else
for x in range(1, 5):
    if x == 5: print('find 5');
else: print('can not find 5!') # can not find 5!
##################################################################
## * 去掉第一层 list 的外套
print(*[1, 2]); print(*[[1, 2]])  # 1 2; [1, 2]
print(*([(1, 2)]))  # (1, 2)
