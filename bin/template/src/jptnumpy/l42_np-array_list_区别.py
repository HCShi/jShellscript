import numpy as np
##################################################################
## slice 切片索引
a = np.array([[1, 2, 3], [4, 5, 6]])
print(a, '\n', a[:2], '\n', a[:2, :1])  # 可以用 a[:2, :1], 第一个参数针对行, 第二个参数是列
b = [[1, 2, 3,], [4, 5, 6]]
print(b, '\n', b[:2])
# print(b[:2, :])  # 这里会报错
