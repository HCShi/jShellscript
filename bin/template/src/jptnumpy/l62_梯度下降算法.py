# 目标函数: y = x**3;  导数: y = 3x**2
def update(x, n, rate):
    for i in range(n):
        print('x: ', x, ' 梯度: ', 3*x**2)
        x = x - rate * 3 * x**2
update(3, 100, 0.1)  # 最后越来越接近 0, 然而 0 还不是极值
