#!/bin/bash
##################################################################
## pidof
pidof ping  # 查看当前命令的进程
##################################################################
## 使用 kill 的脚本杀死另一个脚本
# 脚本一
ping www.baidu.com
# 脚本二
kill $(pidof ping)
# 分别执行以上脚本, 会发现第二个脚本能 kill 第一个脚本
# 因为 ./*.sh 默认调用的是 source, 相当于引入到了父进程中
